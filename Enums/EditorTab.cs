
namespace TVP.Enums
{
    /// <summary>
    /// The selected Tab of BankAccount Editor upon opening
    /// </summary>
    public enum EditorTab
    {
        /// <summary>
        /// The default tab will be selected first (Username Tab)
        /// </summary>
        Default = 0,
        /// <summary>
        /// The username change tab will be selected first
        /// </summary>
        Username,
        /// <summary>
        /// The password change tab will be selected first
        /// </summary>
        Password
    }
}
